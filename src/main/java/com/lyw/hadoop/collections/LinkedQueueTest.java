    public boolean isEmpty() {
        return first() == null;
    }

    public int size() {
        int count = 0;
        for (Test<E> p = first(); p != null; p = succ(p))
            if (p.item != null)
                // Collection.size() spec says to max out
                if (++count == Integer.MAX_VALUE)
                    break;
        return count;
    }

